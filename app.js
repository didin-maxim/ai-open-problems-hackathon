function render() {
  const route = parseRoute();
  app.innerHTML = "";
  app.focus({ preventScroll: true });

  if (route.name === "task") {
    renderTask(route.id);
    typesetMath();
    return;
  }

  if (route.name === "suggest") {
    renderSuggest();
    typesetMath();
    return;
  }

  if (route.name === "about") {
    renderAbout();
    typesetMath();
    return;
  }

  renderHome();
  typesetMath();
}

function parseRoute() {
  const hash = window.location.hash || "#/";
  const [path] = hash.replace(/^#\/?/, "").split("?");
  const parts = path.split("/").filter(Boolean);
  if (parts[0] === "task" && parts[1]) return { name: "task", id: parts[1] };
  if (parts[0] === "suggest") return { name: "suggest" };
  if (parts[0] === "about") return { name: "about" };
  return { name: "home" };
}

