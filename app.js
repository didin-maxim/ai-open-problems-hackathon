async function copyText(text, button) {
  try {
    await navigator.clipboard.writeText(text);
    const initial = button.textContent;
    button.textContent = "Скопировано";
    setTimeout(() => {
      button.textContent = initial;
    }, 1400);
  } catch {
    button.textContent = "Не удалось";
  }
}

