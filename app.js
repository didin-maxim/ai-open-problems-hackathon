function getFilteredTasks() {
  const needle = state.query.trim().toLowerCase();
  return TASKS.filter((task) => {
    const haystack = [task.title, task.summary, task.area, task.author, task.year].join(" ").toLowerCase();
    return (
      (!needle || haystack.includes(needle)) &&
      (state.status === "all" || task.status === state.status) &&
      (state.area === "all" || task.area === state.area)
    );
  }).sort((a, b) => {
    if (state.sort === "old") return numericYear(a.year) - numericYear(b.year);
    if (state.sort === "status") return STATUSES[a.status].label.localeCompare(STATUSES[b.status].label, "ru");
    return numericYear(b.year) - numericYear(a.year);
  });
}

function numericYear(year) {
  const value = Number(year);
  return Number.isFinite(value) ? value : 0;
}

function uniqueAreas() {
  return [...new Set(TASKS.map((task) => task.area))].sort((a, b) => a.localeCompare(b, "ru"));
}

function countActiveProgress() {
  return TASKS.reduce((total, task) => total + task.progress.filter((item) => item.state !== "нет активной команды").length, 0);
}

